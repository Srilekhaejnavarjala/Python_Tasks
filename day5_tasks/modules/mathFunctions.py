"""3. Write a Python program using the math module to
calculate and display the square root, floor value, and
ceiling value of a number entered by the user."""
import math
number = int(input("enter number: "))
print("The square root of the number is: ",math.sqrt(number))
print("The floor value of the number is: ",math.floor(number))
print("The ceiling value of the number is: ",math.ceil(number))
