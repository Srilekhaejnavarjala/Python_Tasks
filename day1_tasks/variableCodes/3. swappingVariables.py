"""3. Swap the values of two variables."""
a = 10
b = 20
print("Value of 'a' before swapping is:",a)
print("Value of 'b' before swapping is:",b)
print("______________________________________________\n")
c = a
a = b
b = c
print("Value of 'a' after swapping is:",a)
print("Value of 'b' before swapping is:",b)
