""" 1. Convert an integer to a float.
    2. Convert a float to an integer."""


#1. Convert a integer to a float.
num = 10
print(f"The type of number {num} is: ",type(num))
n = float(num)
print(f"The type of number {num} after type casting is:",n,type(n))
print("_________________________________________________________________\n")

#2. Convert a float to an integer.
var = 20.0
print(f"The type of number {var} is:",type(var))
v = int(var)
print(f"The type of number {var} after type casting is:",v,type(v))
