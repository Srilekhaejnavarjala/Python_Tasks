"""8. Write a program to find the maximum and minimum values in a tuple."""
tup = (12,23,34,54,66,88,45,31,56)
maximum_value = max(tup)
minimum_value = min(tup)
print("The tuple values are: ",tup)
print("-----------------------------------------\n")
print("The maximum value of a tuple are: ",maximum_value)
print("-----------------------------------------\n")
print("The minimum value of a tuple are: ",minimum_value)
